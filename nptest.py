def calc(dep, mat):
    starts = mat[:,0]
    periods = mat[:,1]
    next_train_idxs = np.maximum(0, np.ceil((dep - starts)/periods))
    next_train_times = starts + (next_train_idxs * periods)
    idx = np.argmin(next_train_times)
    return idx, next_train_times[idx]